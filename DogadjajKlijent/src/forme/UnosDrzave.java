    private void btnUnesiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnesiActionPerformed
        // TODO add your handling code here:
        if(txtNaziv.getText().isEmpty() || cmbKontinent.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(this, "Potrebno je popuniti sva polja!");
            return;
        }
        
        String naziv = txtNaziv.getText();
        String kontinent = (String) cmbKontinent.getSelectedItem();
        Drzava drzava = new Drzava();
        drzava.setNaziv(naziv);
        drzava.setKontinent(kontinent);
        
        //validacija da li postoji
        ModelTabeleDrzava mtd = (ModelTabeleDrzava) tabelaDrzava.getModel();
        boolean postoji = mtd.daLiPostoji(drzava);
        
        if(!postoji) {
        Kontroler.getInstance().dodajDrzavuUBazu(drzava);
        listaDrzava.add(drzava);
        srediTabelu();
        resetujPolja();
        } else {
            JOptionPane.showMessageDialog(this, "Već postoji ova država!");
            return;
        }
    }//GEN-LAST:event_btnUnesiActionPerformed

