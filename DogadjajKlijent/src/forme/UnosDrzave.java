    private void filter(String tekst) {
        ModelTabeleDrzava model = (ModelTabeleDrzava) tabelaDrzava.getModel();
        TableRowSorter<ModelTabeleDrzava> tr = new TableRowSorter<ModelTabeleDrzava>(model);
        tabelaDrzava.setRowSorter(tr);
        
        tr.setRowFilter(RowFilter.regexFilter(tekst));
    }
    
