    private void popuniCombo() {
        ArrayList<String> lista = new ArrayList<>();
        lista.add("Evropa");
        lista.add("Afrika");
        lista.add("Azija");
        lista.add("Južna Amerika");
        lista.add("Severna Amerika");
        cmbKontinent.removeAllItems();
        for(String s : lista) {
            cmbKontinent.addItem(s);
        }
        cmbKontinent.setSelectedIndex(-1);
    }

    public void unesiDrzavu(ServerskiOdgovor so) {
        if (so.isFleg()) {
            JOptionPane.showMessageDialog(this, so.getPoruka());
        } else {
            JOptionPane.showMessageDialog(this, so.getPoruka());           
        }
    }
    
    public void srediTabelu() {
        ModelTabeleDrzava mtd = new ModelTabeleDrzava(listaDrzava);
        tabelaDrzava.setModel(mtd);
    }

    private void resetujPolja() {
        txtNaziv.setText("");
        cmbKontinent.setSelectedItem(-1);
    }

    public void vratiDrzave(ServerskiOdgovor so) {
        if (so.isFleg()) {            
            System.out.println();
            listaDrzava =  (ArrayList<Drzava>) so.getOdgovor();
            srediTabelu();
        } else {
            JOptionPane.showMessageDialog(this, "Neuspešno učitavanje država!");
        }
    }
