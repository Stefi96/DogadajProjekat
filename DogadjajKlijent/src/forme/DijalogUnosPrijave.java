    private void btnPrijavaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrijavaActionPerformed
        // TODO add your handling code here:
        if(txtMotivaciono.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Motivaciono pismoje obavezna!");
            return;
        }
        UnosPrijave ua = (UnosPrijave) this.getParent();
        String motivaciono = txtMotivaciono.getText();
        Dogadjaj dogadjaj = ua.getD();
        dogadjaj.setBrojPrijava(dogadjaj.getBrojPrijava()+1);
        Prijava prijava = new Prijava();
        prijava.setMotivaciono(motivaciono);
        prijava.setDogadaj(dogadjaj);
        Clan clan = ua.getClanNovi();
        clan.setStatus("Aplicirao");        
        prijava.setClan(clan);        
       // Kontroler.getInstance().dodajAplikacijuUBazu(aplik);
        prijava.setStatus("U");
        ua.getListaPrijava().add(prijava); 
        clan.getListaPrijava().add(prijava);
        //prakt.setListaAplikacija(ua.getListaAplikacija());
       // ua.getListaZaCuvanje().add(aplik);
        ua.srediTabeluPrijave();
        this.dispose();
    }//GEN-LAST:event_btnPrijavaActionPerformed

